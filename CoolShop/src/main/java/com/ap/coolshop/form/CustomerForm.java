    private void setTableData(Iterable<ICostumer> records){
        String[] columns = new String [] {"Id","Customer Name", "Phone number", "Bill Amount", "Bill date", "Address","Customer Type"};
        
        try {
            //System.out.println(records);
            Iterator<ICostumer> it = records.iterator();
            Iterator<ICostumer> it1 = records.iterator();
            
            int size = 0;
            while(it.hasNext()){
                it.next();
                size++;
            }
            
            
            Object[][] data = new Object[size][7];
            
            int i=0;
            
            while(it1.hasNext()){
                ICostumer cust = it1.next();
                data[i][0] = cust.getId();
                data[i][1] = cust.getCustomerName();
                data[i][2] = cust.getPhoneNumber();
                data[i][3] = cust.getBillAmount();
                data[i][4] = cust.getBillDate();
                data[i][5] = cust.getAddress();
                data[i][6] = cust.getCustomerType();
                i++;
            }
            TableModel tm =  new javax.swing.table.DefaultTableModel(data,columns);
            table_data.setModel(tm);
            
            table_data.addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    int row = table_data.rowAtPoint(evt.getPoint());
                    int col = table_data.columnAtPoint(evt.getPoint());
                    if (row >= 0 && col >= 0) {
                        
                        String custType = data[row][6].toString();
                        try {
                            cust = dal.getData(row);
                            setFormData();
                        } catch (Exception ex) {
                            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            });

            
        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

