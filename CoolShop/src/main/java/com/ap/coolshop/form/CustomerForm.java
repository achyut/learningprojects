    private void setCustomer() throws Exception{
        String cus = combo_customerType.getSelectedItem().toString();
        if(cust==null){
            cust = Factory.getObject(cus);
        }
        cust.setCustomerType(cus);
        cust.setCustomerName(edit_customerName.getText());
        cust.setAddress(edit_address.getText());
        if(!edit_billAmount.getText().trim().isEmpty()){
            cust.setBillAmount(Double.parseDouble(edit_billAmount.getText()));
        }
        cust.setPhoneNumber(edit_phoneNumber.getText());
        cust.setBillDate(edit_billDate.getText());
    }
    
    private void clearForm(){
        edit_customerName.setText("");
        edit_address.setText("");
        edit_billAmount.setText("");
        edit_billDate.setText(formatter.format(new Date()));
        edit_phoneNumber.setText("");
    }
    
    private void setFormData(){
        //System.out.println(cust.getCustomerName());
        
        edit_customerName.setText(cust.getCustomerName());
        edit_address.setText(cust.getAddress());
        if(cust.getBillAmount()!=null){
            edit_billAmount.setText(cust.getBillAmount().toString());
        }
        edit_billDate.setText(cust.getBillDate());
        edit_phoneNumber.setText(cust.getPhoneNumber());
        combo_customerType.setSelectedItem(cust.getCustomerType());
    }
    
